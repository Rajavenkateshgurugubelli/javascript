// Assignment Operator: '='
// The assignment operator '=' is used to assign a value to a variable.

// Example 1: Assigning values to variables 'a' and 'b'
let a = 1;
let b = 3;

// Example 2: Add and Assign Operator: '+='
// The add and assign operator '+=', adds the value on the right to the variable on the left and assigns the result to the variable.
console.log(a += 10); // Output: 11
// Explanation: Here, we add 10 to the value of 'a', which is 1, and assign the result (11) back to 'a'.

// Example 3: Subtract and Assign Operator: '-='
// The subtract and assign operator '-=', subtracts the value on the right from the variable on the left and assigns the result to the variable.
console.log(a -= 5); // Output: 6
// Explanation: Here, we subtract 5 from the value of 'a', which is 11 (from the previous operation), and assign the result (6) back to 'a'.

// Example 4: Multiply and Assign Operator: '*='
// The multiply and assign operator '*=', multiplies the value on the right with the variable on the left and assigns the result to the variable.
console.log(a *= 11); // Output: 66
// Explanation: Here, we multiply the value of 'a', which is 6 (from the previous operation), by 11, and assign the result (66) back to 'a'.

// Example 5: Divide and Assign Operator: '/='
// The divide and assign operator '/=', divides the variable on the left by the value on the right and assigns the result to the variable.
console.log(a /= 2); // Output: 33
// Explanation: Here, we divide the value of 'a', which is 66 (from the previous operation), by 2, and assign the result (33) back to 'a'.

// Example 6: Modulus and Assign Operator: '%='
// The modulus and assign operator '%=', divides the variable on the left by the value on the right and assigns the remainder to the variable.
console.log(a %= 2); // Output: 1
// Explanation: Here, we calculate the remainder when the value of 'a', which is 33 (from the previous operation), is divided by 2, and assign the result (1) back to 'a'.

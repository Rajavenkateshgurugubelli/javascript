// The bitwise operations are performed on bit level like binary on operands
// the operands are converted to binary and operations are performed later the result was logged

// 1.Bitwise AND (&)
console.log('AND',10==20 & 20==33) // 0 

// 2.Bitwise OR(|)
console.log('OR',10==20 | 20==33) //0

// 3.Bitwise XOR(^)
console.log('XOR',10==20 ^ 20==33) //0

// 4.Bitwise NOT(~)
console.log('NOT',~10)  //-11

// 5.Bitwise Left Shift
console.log('left shift',10<<2) //40

// 6.Bitwise Right Shift
console.log('Right shift', 10>>2) //2
